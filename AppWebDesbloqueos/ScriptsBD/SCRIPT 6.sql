CREATE PROCEDURE INSERTAR_CN
@CN VARCHAR(100)
AS
BEGIN
	INSERT INTO CN VALUES (@CN)
END;

CREATE PROCEDURE EDITAR_CN
@ID INT,
@CN VARCHAR(100)
AS
BEGIN
	UPDATE CN SET CN=@CN WHERE ID = @ID
END;

CREATE PROCEDURE ELIMINAR_CN
@ID INT
AS
BEGIN
	DELETE FROM CN WHERE ID = @ID
END;

CREATE PROCEDURE CONSULTAR_CN
AS
BEGIN
	SELECT ID, CN FROM CN
END;

CREATE PROCEDURE CONSULTAR_ID_CN
@ID INT
AS
BEGIN
	SELECT ID, CN FROM CN WHERE ID = @ID
END;